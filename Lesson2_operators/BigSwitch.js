//  Присвоить переменной а значение в промежутке [0..15]. С помощью оператора switch
//  организовать вывод чисел от a до 15.
function BigSwitch(a) {
    switch (a) {
        case 0:
            return "[0..15]";
        case 1:
            return "[1..15]";
        case 2:
            return "[2..15]";
        case 3:
            return "[3..15]";
        case 4:
            return "[4..15]";
        case 5:
            return "[5..15]";
        case 6:
            return "[6..15]";
        case 7:
            return "[7..15]";
        case 8:
            return "[8..15]";
        case 9:
            return "[9..15]";
        case 10:
            return "[10..15]";
        case 11:
            return "[11..15]";
        case 12:
            return "[12..15]";
        case 13:
            return "[13..15]";
        case 14:
            return "[14..15]";
        case 15:
            return "[15]";
        default:
            break;
    }
}
